import re


def replace_v_with_g(text: str) -> str:
    """
    Заменяет 'в' на 'г' в словах, где 'в' находится между двумя гласными.
    
    Примеры:
    - "слово" -> "слого"
    - "говорить" -> "гогорить"
    - "привет" -> "пригет"
    - "вода" -> "вода" (в в начале, не между гласными)
    """
    vowels = 'аеёиоуыэюяАЕЁИОУЫЭЮЯ'
    
    # Паттерн: гласная + 'в' + гласная
    pattern = f'([{vowels}])в([{vowels}])'
    
    # Заменяем 'в' на 'г', сохраняя гласные
    result = re.sub(pattern, r'\1г\2', text, flags=re.IGNORECASE)
    
    return result


def main():
    # Примеры для теста
    test_texts = [
        "слово",
        "говорить",
        "привет",
        "вода",
        "в словах где есть гласная в гласная заменять в на г",
        "новый уровень",
        "Москва",
        "правительство"
    ]
    
    print("Замена 'в' на 'г' между гласными:\n")
    for text in test_texts:
        result = replace_v_with_g(text)
        print(f"'{text}' -> '{result}'")


if __name__ == "__main__":
    main()
