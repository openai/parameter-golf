def zeropower_via_newtonschulz5(
    G: Tensor, steps: int = 10, eps: float = 1e-7
) -> Tensor:
    a, b, c = (3.4445, -4.7750, 2.0315)
    X = G.bfloat16()
    X /= X.norm() + eps
    transposed = G.size(0) > G.size(1)
    if transposed:
        X = X.T
    for _ in range(steps):
        A = X @ X.T
        B = b * A + c * A @ A
        X = a * X + B @ X
    return X.T if transposed else X


class Muon(torch.optim.Optimizer):
    def __init__(
        self,
        params,
        lr: float,
        momentum: float,
        backend_steps: int,
        nesterov: bool = True,
    ):
        super().__init__(
            params,
            dict(
                lr=lr, momentum=momentum, backend_steps=backend_steps, nesterov=nesterov
            ),
        )
        self._group_meta: list[dict[str, object]] = []
        for group_idx, group in enumerate(self.param_groups):
            group_params = group["params"]
            numels = [int(p.numel()) for p in group_params]
            self._group_meta.append({
                "numels": numels,
                "total_params": sum(numels),
                "flat_buffer": None,
            })

    @torch.no_grad()
    def step(self, closure=None):
        loss = None
        if closure is not None:
            with torch.enable_grad():
                loss = closure()

        distributed = dist.is_available() and dist.is_initialized()
        world_size = dist.get_world_size() if distributed else 1
        rank = dist.get_rank() if distributed else 0

        for group_idx, group in enumerate(self.param_groups):
            params = group["params"]
            if not params:
                continue
            lr = group["lr"]
            momentum = group["momentum"]
            backend_steps = group["backend_steps"]
            nesterov = group["nesterov"]
            meta = self._group_meta[group_idx]
            numels: list[int] = meta["numels"]  # type: ignore[assignment]
            total_params: int = meta["total_params"]  # type: ignore[assignment]
            updates_flat = meta["flat_buffer"]

            if (
                updates_flat is None
                or updates_flat.device != params[0].device
                or updates_flat.numel() != total_params
            ):
                updates_flat = torch.empty(
                    total_params, device=params[0].device, dtype=torch.bfloat16
                )
                meta["flat_buffer"] = updates_flat
            updates_flat.zero_()

            curr = 0
            for i, (p, p_numel) in enumerate(zip(params, numels, strict=True)):
                if i % world_size == rank and p.grad is not None:
                    g = p.grad
                    state = self.state[p]
                    if "momentum_buffer" not in state:
                        state["momentum_buffer"] = torch.zeros_like(g)
                    buf = state["momentum_buffer"]
                    buf.mul_(momentum).add_(g)
                    if nesterov:
                        g = g.add(buf, alpha=momentum)
                    g = zeropower_via_newtonschulz5(g, steps=backend_steps)
                    g *= max(1, g.size(0) / g.size(1)) ** 0.5
                    updates_flat[curr : curr + p_numel] = g.reshape(-1)
                curr += p_numel

            if distributed:
                dist.all_reduce(updates_flat, op=dist.ReduceOp.SUM)

            curr = 0
            for p, p_numel in zip(params, numels, strict=True):
                g = updates_flat[curr : curr + p_numel].view_as(p).to(dtype=p.dtype)
                p.add_(g, alpha=-lr)
                curr += p_numel

        return loss


