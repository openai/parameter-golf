def load_data_shard(file: Path) -> Tensor:
    header_bytes = 256 * np.dtype("<i4").itemsize
    token_bytes = np.dtype("<u2").itemsize
    header = np.fromfile(file, dtype="<i4", count=256)
    if header.size != 256 or int(header[0]) != 20240520 or int(header[1]) != 1:
        raise ValueError(f"Unexpected shard header for {file}")
    num_tokens = int(header[2])
    expected_size = header_bytes + num_tokens * token_bytes
    if file.stat().st_size != expected_size:
        raise ValueError(
            f"Shard size mismatch for {file}: expected {expected_size} bytes"
        )
    tokens_np = np.fromfile(file, dtype="<u2", count=num_tokens, offset=header_bytes)
    if tokens_np.size != num_tokens:
        raise ValueError(f"Short read for {file}")
    return torch.from_numpy(tokens_np.astype(np.uint16, copy=False))


def resolve_train_shards(args: Hyperparameters) -> list[Path]:
    files = [Path(p) for p in sorted(glob.glob(args.train_files))]
    if not files:
        raise FileNotFoundError(f"No files found for pattern: {args.train_files}")
    if args.train_shard_stride <= 0:
        raise ValueError(
            f"TRAIN_SHARD_STRIDE must be positive, got {args.train_shard_stride}"
        )
    if args.train_shard_offset < 0:
        raise ValueError(
            f"TRAIN_SHARD_OFFSET must be non-negative, got {args.train_shard_offset}"
        )
    selected = files[args.train_shard_offset :: args.train_shard_stride]
    if args.train_max_shards > 0:
        selected = selected[: args.train_max_shards]
    if not selected:
        raise ValueError(
            "Train shard selection is empty; adjust TRAIN_SHARD_OFFSET / "
            "TRAIN_SHARD_STRIDE / TRAIN_MAX_SHARDS"
        )
    return selected


class TokenStream:
    # Reads shards sequentially and wraps around forever. The training loop therefore
    # has deterministic, simple streaming behavior with no sampling or workers.
    def __init__(self, files: list[Path], preload_all: bool = False):
        self.files = files
        if not self.files:
            raise FileNotFoundError("No training shards available")
        self.file_idx = 0
        self.preload_all = preload_all
        self.shards: list[Tensor] | None = None

        if self.preload_all:
            self.shards = [load_data_shard(file) for file in self.files]
            self.tokens = self.shards[0]
        else:
            self.tokens = load_data_shard(self.files[0])
        self.pos = 0

    def _advance_file(self) -> None:
        self.file_idx = (self.file_idx + 1) % len(self.files)
        if self.shards is not None:
            self.tokens = self.shards[self.file_idx]
        else:
            self.tokens = load_data_shard(self.files[self.file_idx])
        self.pos = 0

    def take(self, n: int) -> Tensor:
        chunks: list[Tensor] = []
        remaining = n
        while remaining > 0:
            avail = self.tokens.numel() - self.pos
            if avail <= 0:
                self._advance_file()
                continue
            k = min(remaining, avail)
            chunks.append(self.tokens[self.pos : self.pos + k])
            self.pos += k
            remaining -= k
        return chunks[0] if len(chunks) == 1 else torch.cat(chunks)


class DistributedTokenLoader:
    # Each call consumes a contiguous chunk from the shared token stream, then slices out
    # one disjoint span per rank. The extra "+1" token lets us build (x, y) by shifting.
    def __init__(
        self,
        rank: int,
        world_size: int,
        device: torch.device,
        files: list[Path],
        preload_all: bool = False,
        pin_batches: bool = True,
    ):
        self.rank = rank
        self.world_size = world_size
        self.device = device
        self.pin_batches = pin_batches
        self.stream = TokenStream(files, preload_all=preload_all)

    def next_batch(
        self, global_tokens: int, seq_len: int, grad_accum_steps: int
    ) -> tuple[Tensor, Tensor]:
        local_tokens = global_tokens // (self.world_size * grad_accum_steps)
        per_rank_span = local_tokens + 1
        chunk = self.stream.take(per_rank_span * self.world_size)
        start = self.rank * per_rank_span
        local = chunk[start : start + per_rank_span]
        x_cpu = local[:-1].reshape(-1, seq_len)
        y_cpu = local[1:].reshape(-1, seq_len)

        if self.pin_batches:
            x_cpu = x_cpu.pin_memory()
            y_cpu = y_cpu.pin_memory()

        x = x_cpu.to(self.device, non_blocking=self.pin_batches).long()
        y = y_cpu.to(self.device, non_blocking=self.pin_batches).long()
        return x, y


