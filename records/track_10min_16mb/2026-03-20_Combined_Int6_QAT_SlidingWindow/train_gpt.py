def build_bigram_table(train_files_pattern: str, vocab_size: int = 1024, max_shards: int = 10) -> Tensor:
    """Build [vocab, vocab] residual bigram bias table from training data."""
    files = sorted(glob.glob(train_files_pattern))[:max_shards]
    if not files:
        return torch.zeros(vocab_size, vocab_size, dtype=torch.float16)
    bigram_count = torch.zeros(vocab_size, vocab_size, dtype=torch.float64)
    for f in files:
        tokens = load_data_shard(Path(f)).long()
        prev, nxt = tokens[:-1], tokens[1:]
        idx = prev * vocab_size + nxt
        bigram_count.view(-1).scatter_add_(0, idx, torch.ones_like(idx, dtype=torch.float64))
    unigram_count = bigram_count.sum(dim=0)
    unigram_prob = (unigram_count + 1.0) / (unigram_count + 1.0).sum()
    row_sums = bigram_count.sum(dim=1, keepdim=True).clamp_min(1.0)
    bigram_prob = (bigram_count + 1e-8) / row_sums
    log_bias = torch.log(bigram_prob) - torch.log(unigram_prob[None, :])
    return log_bias.to(torch.float16)


