class RMSNorm(nn.Module):
    def __init__(self, eps: float | None = None):
        super().__init__()
        self.eps = eps

    def forward(self, x: Tensor) -> Tensor:
        return F.rms_norm(x, (x.size(-1),), eps=self.eps)


# Global QAT state: set by training loop to enable fake quantization in the second half of training.
_qat_enabled = False
# QAT quantization levels: 31 = int6, 127 = int8.
_qat_levels = int(os.environ.get("QUANT_LEVELS", "127"))

def fake_quantize_per_row(w: Tensor) -> Tensor:
    # Straight-through estimator: simulate int6/int8 per-row quantization in forward pass,
    # but pass gradients through as if no quantization happened.
    levels = _qat_levels
    scale = w.abs().amax(dim=1, keepdim=True) / float(levels)
    scale = scale.clamp_min(1e-12)
    w_q = (w / scale).round().clamp(-levels, levels)
    return (w_q * scale - w).detach() + w  # STE: gradient flows through


class CastedLinear(nn.Linear):
    # Keep weights in fp32 for optimizer/state quality, cast at matmul time for bf16 compute.
    def forward(self, x: Tensor) -> Tensor:
        w = self.weight
        if self.training and _qat_enabled and w.ndim == 2:
            w = fake_quantize_per_row(w)
        bias = self.bias.to(x.dtype) if self.bias is not None else None
        out = F.linear(x, w.to(x.dtype), bias)
        if hasattr(self, "lora_A"):
            out = out + (x.float() @ self.lora_A @ self.lora_B).to(out.dtype)
        return out


def apply_lora_adapters(model: nn.Module, rank: int, device: torch.device) -> list[nn.Parameter]:
    """Add LoRA A/B adapters to every CastedLinear. Returns trainable LoRA params."""
    torch.manual_seed(42)  # Reproducible across distributed ranks
    params: list[nn.Parameter] = []
    for m in model.modules():
        if isinstance(m, CastedLinear):
            A = nn.Parameter(torch.randn(m.in_features, rank, device=device, dtype=torch.float32) * (1.0 / rank))
            B = nn.Parameter(torch.zeros(rank, m.out_features, device=device, dtype=torch.float32))
            m.lora_A, m.lora_B = A, B
            params.extend([A, B])
    return params


def remove_lora_adapters(model: nn.Module) -> None:
    for m in model.modules():
        if isinstance(m, CastedLinear) and hasattr(m, "lora_A"):
            del m.lora_A, m.lora_B


def ttt_adapt(
    model: nn.Module, val_tokens: Tensor, seq_len: int, device: torch.device,
    lora_params: list, lr: float, steps: int, log_fn=None,
) -> None:
    """Online learning: train LoRA adapters on val tokens before scoring."""
    total_tokens = val_tokens.numel() - 1
    n_chunks = total_tokens // seq_len
    if steps <= 0:
        steps = n_chunks
    optimizer = torch.optim.Adam(lora_params, lr=lr)
    frozen: list[nn.Parameter] = []
    lora_set = set(id(p) for p in lora_params)
    for p in model.parameters():
        if id(p) not in lora_set and p.requires_grad:
            p.requires_grad_(False)
            frozen.append(p)
    model.train()
    t0 = time.perf_counter()
    for step in range(steps):
        start = (step % n_chunks) * seq_len
        chunk = val_tokens[start : start + seq_len + 1].to(dtype=torch.int64, device=device)
        x, y = chunk[:-1].unsqueeze(0), chunk[1:].unsqueeze(0)
        with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
            loss = model(x, y)
        optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(lora_params, 1.0)
        optimizer.step()
        if log_fn and (step % 500 == 0 or step == steps - 1):
            log_fn(f"ttt step:{step}/{steps} loss:{loss.item():.4f} time:{time.perf_counter() - t0:.1f}s")
    for p in frozen:
        p.requires_grad_(True)


def restore_low_dim_params_to_fp32(module: nn.Module) -> None:
    # Keep small/control parameters in fp32 even when the model body runs in bf16.
    with torch.no_grad():
        for name, param in module.named_parameters():
            if (param.ndim < 2 or any(pattern in name for pattern in CONTROL_TENSOR_NAME_PATTERNS)) and param.dtype != torch.float32:
                param.data = param.data.float()


class Rotary(nn.Module):
    # Caches cos/sin tables per sequence length on the current device.
    # Supports NTK-aware scaling for eval sequences longer than training.
    def __init__(self, dim: int, base: float = 10000.0, train_seq_len: int = 1024):
        super().__init__()
        self.dim = dim
        self.base = base
        self.train_seq_len = train_seq_len
        inv_freq = 1.0 / (base ** (torch.arange(0, dim, 2, dtype=torch.float32) / dim))
        self.register_buffer("inv_freq", inv_freq, persistent=False)
        self._seq_len_cached = 0
        self._cos_cached: Tensor | None = None
        self._sin_cached: Tensor | None = None

    def forward(self, seq_len: int, device: torch.device, dtype: torch.dtype) -> tuple[Tensor, Tensor]:
        if (
            self._cos_cached is None
            or self._sin_cached is None
            or self._seq_len_cached != seq_len
            or self._cos_cached.device != device
        ):
            inv_freq = self.inv_freq.to(device)
            if seq_len > self.train_seq_len:
                # NTK-aware RoPE scaling: increase base frequency proportionally
                scale = seq_len / self.train_seq_len
                new_base = self.base * scale
                inv_freq = 1.0 / (new_base ** (torch.arange(0, self.dim, 2, device=device, dtype=torch.float32) / self.dim))
            t = torch.arange(seq_len, device=device, dtype=inv_freq.dtype)
            freqs = torch.outer(t, inv_freq)
            self._cos_cached = freqs.cos()[None, None, :, :]
            self._sin_cached = freqs.sin()[None, None, :, :]
            self._seq_len_cached = seq_len
        return self._cos_cached.to(dtype=dtype), self._sin_cached.to(dtype=dtype)


def apply_rotary_emb(x: Tensor, cos: Tensor, sin: Tensor) -> Tensor:
    half = x.size(-1) // 2
    x1, x2 = x[..., :half], x[..., half:]
    return torch.cat((x1 * cos + x2 * sin, x1 * (-sin) + x2 * cos), dim=-1)


class CausalSelfAttention(nn.Module):
    def __init__(
        self,
        dim: int,
        num_heads: int,
        num_kv_heads: int,
        rope_base: float,
        qk_gain_init: float,
        train_seq_len: int = 1024,
    ):
        super().__init__()
        if dim % num_heads != 0:
            raise ValueError("model_dim must be divisible by num_heads")
        if num_heads % num_kv_heads != 0:
            raise ValueError("num_heads must be divisible by num_kv_heads")
        self.num_heads = num_heads
        self.num_kv_heads = num_kv_heads
        self.head_dim = dim // num_heads
        if self.head_dim % 2 != 0:
            raise ValueError("head_dim must be even for RoPE")
        kv_dim = self.num_kv_heads * self.head_dim
        self.c_q = CastedLinear(dim, dim, bias=False)
        self.c_k = CastedLinear(dim, kv_dim, bias=False)
        self.c_v = CastedLinear(dim, kv_dim, bias=False)
        self.proj = CastedLinear(dim, dim, bias=False)
        self.proj._zero_init = True
        self.q_gain = nn.Parameter(torch.full((num_heads,), qk_gain_init, dtype=torch.float32))
        self.rotary = Rotary(self.head_dim, base=rope_base, train_seq_len=train_seq_len)

    def forward(self, x: Tensor) -> Tensor:
        bsz, seqlen, dim = x.shape
        q = self.c_q(x).reshape(bsz, seqlen, self.num_heads, self.head_dim).transpose(1, 2)
        k = self.c_k(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)
        v = self.c_v(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)
        q = F.rms_norm(q, (q.size(-1),))
        k = F.rms_norm(k, (k.size(-1),))
        cos, sin = self.rotary(seqlen, x.device, q.dtype)
        q = apply_rotary_emb(q, cos, sin)
        k = apply_rotary_emb(k, cos, sin)
        q = q * self.q_gain.to(dtype=q.dtype)[None, :, None, None]
        y = F.scaled_dot_product_attention(
            q,
            k,
            v,
            attn_mask=None,
            is_causal=True,
            enable_gqa=(self.num_kv_heads != self.num_heads),
        )
        y = y.transpose(1, 2).contiguous().reshape(bsz, seqlen, dim)
        return self.proj(y)


class MLP(nn.Module):
    # Supports relu^2 (default) and parameter-rebalanced swiglu.
    def __init__(self, dim: int, mlp_mult: int, variant: str = "relu2"):
        super().__init__()
        self.variant = variant
        if variant == "swiglu":
            # SwiGLU has 3 projection matrices vs 2 for relu^2.
            # Use ~2/3 hidden width to keep params roughly comparable.
            hidden = max(1, int(round((2.0 / 3.0) * mlp_mult * dim)))
            self.fc = CastedLinear(dim, hidden * 2, bias=False)
            self.proj = CastedLinear(hidden, dim, bias=False)
        else:
            hidden = mlp_mult * dim
            self.fc = CastedLinear(dim, hidden, bias=False)
            self.proj = CastedLinear(hidden, dim, bias=False)
        self.proj._zero_init = True

    def forward(self, x: Tensor) -> Tensor:
        if self.variant == "swiglu":
            x = self.fc(x)
            a, b = x.chunk(2, dim=-1)
            return self.proj(F.silu(a) * b)
        x = torch.relu(self.fc(x))
        return self.proj(x.square())


class Block(nn.Module):
    def __init__(
        self,
        dim: int,
        num_heads: int,
        num_kv_heads: int,
        mlp_mult: int,
        mlp_variant: str,
        rope_base: float,
        qk_gain_init: float,
        train_seq_len: int = 1024,
    ):
        super().__init__()
        self.attn_norm = RMSNorm()
        self.mlp_norm = RMSNorm()
        self.attn = CausalSelfAttention(dim, num_heads, num_kv_heads, rope_base, qk_gain_init, train_seq_len)
        self.mlp = MLP(dim, mlp_mult, variant=mlp_variant)
        self.attn_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.mlp_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.resid_mix = nn.Parameter(torch.stack((torch.ones(dim), torch.zeros(dim))).float())

    def forward(self, x: Tensor, x0: Tensor) -> Tensor:
        mix = self.resid_mix.to(dtype=x.dtype)
        x = mix[0][None, None, :] * x + mix[1][None, None, :] * x0
        attn_out = self.attn(self.attn_norm(x))
        x = x + self.attn_scale.to(dtype=x.dtype)[None, None, :] * attn_out
        x = x + self.mlp_scale.to(dtype=x.dtype)[None, None, :] * self.mlp(self.mlp_norm(x))
        return x


class GPT(nn.Module):
    def __init__(
        self,
        vocab_size: int,
        num_layers: int,
        model_dim: int,
        num_heads: int,
        num_kv_heads: int,
        mlp_mult: int,
        mlp_variant: str,
        tie_embeddings: bool,
        tied_embed_init_std: float,
        logit_softcap: float,
        rope_base: float,
        qk_gain_init: float,
        train_seq_len: int = 1024,
        depth_recurrence: int = 1,
    ):
        super().__init__()
        if logit_softcap <= 0.0:
            raise ValueError(f"logit_softcap must be positive, got {logit_softcap}")
        self.tie_embeddings = tie_embeddings
        self.tied_embed_init_std = tied_embed_init_std
        self.logit_softcap = logit_softcap
        self.tok_emb = nn.Embedding(vocab_size, model_dim)
        self.depth_recurrence = depth_recurrence
        # effective layers = num_physical_blocks * depth_recurrence
        num_physical = num_layers // depth_recurrence if depth_recurrence > 1 else num_layers
        self.num_physical_blocks = num_physical
        effective_layers = num_physical * depth_recurrence
        self.num_encoder_layers = effective_layers // 2
        self.num_decoder_layers = effective_layers - self.num_encoder_layers
        self.num_skip_weights = min(self.num_encoder_layers, self.num_decoder_layers)
        self.skip_weights = nn.Parameter(torch.ones(self.num_skip_weights, model_dim, dtype=torch.float32))
        self.blocks = nn.ModuleList(
            [
                Block(
                    model_dim,
                    num_heads,
                    num_kv_heads,
                    mlp_mult,
                    mlp_variant,
                    rope_base,
                    qk_gain_init,
                    train_seq_len,
                )
                for i in range(num_physical)
            ]
        )
        self.final_norm = RMSNorm()
        self.lm_head = None if tie_embeddings else CastedLinear(model_dim, vocab_size, bias=False)
        if self.lm_head is not None:
            self.lm_head._zero_init = True
        self._init_weights()

    def _init_weights(self) -> None:
        overtone_init = bool(int(os.environ.get("OVERTONE_INIT", "0")))
        phase_resid_mix_init = bool(int(os.environ.get("PHASE_RESID_MIX_INIT", "0")))
        if self.tie_embeddings:
            nn.init.normal_(self.tok_emb.weight, mean=0.0, std=self.tied_embed_init_std)
            if overtone_init:
                with torch.no_grad():
                    u, s, vh = torch.linalg.svd(self.tok_emb.weight.data, full_matrices=False)
                    target_s = s[0] * torch.rsqrt(torch.arange(1, s.shape[0] + 1, dtype=s.dtype))
                    self.tok_emb.weight.data = (u * target_s[None, :]) @ vh
        for module in self.modules():
            if isinstance(module, nn.Linear) and getattr(module, "_zero_init", False):
                nn.init.zeros_(module.weight)
        if phase_resid_mix_init:
            for i, block in enumerate(self.blocks):
                p = torch.sigmoid(torch.tensor(3.0 * (i / max(len(self.blocks) - 1, 1) - 0.5), dtype=torch.float32))
                block.resid_mix.data[0].fill_(float(p))
                block.resid_mix.data[1].fill_(float(1.0 - p))

    def _physical_idx(self, effective_idx: int) -> int:
        """Map effective layer index to physical block index for depth recurrence."""
        return effective_idx % self.num_physical_blocks

    def forward(self, input_ids: Tensor, target_ids: Tensor) -> Tensor:
        x = self.tok_emb(input_ids)
        x = F.rms_norm(x, (x.size(-1),))
        x0 = x
        skips: list[Tensor] = []

        # First half stores skips; second half reuses them in reverse order.
        for i in range(self.num_encoder_layers):
            x = self.blocks[self._physical_idx(i)](x, x0)
            skips.append(x)
        for i in range(self.num_decoder_layers):
            if skips:
                x = x + self.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            x = self.blocks[self._physical_idx(self.num_encoder_layers + i)](x, x0)

        x = self.final_norm(x).reshape(-1, x.size(-1))
        targets = target_ids.reshape(-1)
        if self.tie_embeddings:
            logits_proj = F.linear(x, self.tok_emb.weight)
        else:
            if self.lm_head is None:
                raise RuntimeError("lm_head is required when tie_embeddings=False")
            logits_proj = self.lm_head(x)
        logits = self.logit_softcap * torch.tanh(logits_proj / self.logit_softcap)
        return F.cross_entropy(logits.float(), targets, reduction="mean")

    def forward_logits(self, input_ids: Tensor) -> Tensor:
        """Return logits (bsz, seq_len, vocab) without computing loss."""
        x = self.tok_emb(input_ids)
        x = F.rms_norm(x, (x.size(-1),))
        x0 = x
        skips: list[Tensor] = []
        for i in range(self.num_encoder_layers):
            x = self.blocks[self._physical_idx(i)](x, x0)
            skips.append(x)
        for i in range(self.num_decoder_layers):
            if skips:
                x = x + self.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            x = self.blocks[self._physical_idx(self.num_encoder_layers + i)](x, x0)
        x = self.final_norm(x)
        if self.tie_embeddings:
            logits_proj = F.linear(x, self.tok_emb.weight)
        else:
            logits_proj = self.lm_head(x)
        return self.logit_softcap * torch.tanh(logits_proj / self.logit_softcap)


