"""
train_gpt_www.py — Training script for the WWW (Wavelet-Weighted Widenet) architecture.

Key differences from train_gpt.py:
- Uses WWWGPT model with TernaryLinear MLP layers (STE training)
- 12 layers, 4:1 MLP width ratio (2048 hidden for 512 dim)
- Optimizer split: Adam for ternary MLP weights, Muon for attention matrices
- VQ + int8 hybrid compression: ternary VQ for MLPs, int8 for attention
- ~35.2M params fitting in ~13.8MB (vs 18.9M in ~15.5MB)

Shared components imported from train_gpt.py:
- Muon optimizer, data loading, tokenizer LUTs, evaluation, TTT LoRA
"""

