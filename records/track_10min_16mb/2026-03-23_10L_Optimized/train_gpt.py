class Rotary(nn.Module):
    def __init__(self, dim: int, base: float = 10000.0):
        super().__init__()
        inv_freq = 1.0 / (base ** (torch.arange(0, dim, 2, dtype=torch.float32) / dim))
        self.register_buffer("inv_freq", inv_freq, persistent=False)
        self._seq_len_cached = 0
        self._cos_cached: Tensor | None = None
        self._sin_cached: Tensor | None = None

    def forward(self, seq_len: int, device: torch.device, dtype: torch.dtype) -> tuple[Tensor, Tensor]:
        if (
            self._cos_cached is None
            or self._sin_cached is None
            or self._seq_len_cached != seq_len
            or self._cos_cached.device != device
        ):
            t = torch.arange(seq_len, device=device, dtype=self.inv_freq.dtype)
            freqs = torch.outer(t, self.inv_freq.to(device))
            self._cos_cached = freqs.cos()[None, None, :, :]
            self._sin_cached = freqs.sin()[None, None, :, :]
            self._seq_len_cached = seq_len
        return self._cos_cached.to(dtype=dtype), self._sin_cached.to(dtype=dtype)


def apply_rotary_emb(x: Tensor, cos: Tensor, sin: Tensor) -> Tensor:
    half = x.size(-1) // 2
    x1, x2 = x[..., :half], x[..., half:]
    return torch.cat((x1 * cos + x2 * sin, x1 * (-sin) + x2 * cos), dim=-1)


class CausalSelfAttention(nn.Module):
    """Attention with Value Residual (ResFormer) and Gated Attention."""
    def __init__(self, dim: int, num_heads: int, num_kv_heads: int, rope_base: float,
                 qk_gain_init: float, layer_idx: int = 0):
        super().__init__()
        if dim % num_heads != 0:
            raise ValueError("model_dim must be divisible by num_heads")
        if num_heads % num_kv_heads != 0:
            raise ValueError("num_heads must be divisible by num_kv_heads")
        self.num_heads = num_heads
        self.num_kv_heads = num_kv_heads
        self.head_dim = dim // num_heads
        self.layer_idx = layer_idx
        if self.head_dim % 2 != 0:
            raise ValueError("head_dim must be even for RoPE")
        kv_dim = self.num_kv_heads * self.head_dim
        self.c_q = CastedLinear(dim, dim, bias=False)
        self.c_k = CastedLinear(dim, kv_dim, bias=False)
        self.c_v = CastedLinear(dim, kv_dim, bias=False)
        self.proj = CastedLinear(dim, dim, bias=False)
        self.proj._zero_init = True
        self.q_gain = nn.Parameter(torch.full((num_heads,), qk_gain_init, dtype=torch.float32))
        self.rotary = Rotary(self.head_dim, base=rope_base)

        self.attn_gate = nn.Parameter(torch.full((num_heads,), 4.0, dtype=torch.float32))

        if layer_idx > 0:
            self.v_residual_lambda = nn.Parameter(
                torch.tensor([0.9, 0.1], dtype=torch.float32)
            )
        else:
            self.v_residual_lambda = None

    def forward(self, x: Tensor, v0: Tensor | None = None) -> tuple[Tensor, Tensor]:
        """Returns (output, v) where v is the V vectors for value residual caching."""
        bsz, seqlen, dim = x.shape
        q = self.c_q(x).reshape(bsz, seqlen, self.num_heads, self.head_dim).transpose(1, 2)
        k = self.c_k(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)
        v = self.c_v(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)

        v_out = v  # cache this layer's raw V for layer 0
        if self.v_residual_lambda is not None and v0 is not None:
            lam = self.v_residual_lambda.to(dtype=v.dtype)
            v = lam[0] * v + lam[1] * v0

        q = F.rms_norm(q, (q.size(-1),))
        k = F.rms_norm(k, (k.size(-1),))
        cos, sin = self.rotary(seqlen, x.device, q.dtype)
        q = apply_rotary_emb(q, cos, sin)
        k = apply_rotary_emb(k, cos, sin)
        q = q * self.q_gain.to(dtype=q.dtype)[None, :, None, None]
        y = F.scaled_dot_product_attention(
            q, k, v, attn_mask=None, is_causal=True,
            enable_gqa=(self.num_kv_heads != self.num_heads),
        )

        gate = torch.sigmoid(self.attn_gate.to(dtype=y.dtype))[None, :, None, None]
        y = y * gate

        y = y.transpose(1, 2).contiguous().reshape(bsz, seqlen, dim)
        return self.proj(y), v_out


class MLP(nn.Module):
    def __init__(self, dim: int, mlp_mult: float):
        super().__init__()
        hidden = int(mlp_mult * dim)
        self.fc = CastedLinear(dim, hidden, bias=False)
        self.proj = CastedLinear(hidden, dim, bias=False)
        self.proj._zero_init = True

    def forward(self, x: Tensor) -> Tensor:
        x = F.leaky_relu(self.fc(x), negative_slope=0.5)
        return self.proj(x.square())


class SmearGate(nn.Module):
    """Blend each token's embedding with the previous token's embedding."""
    def __init__(self, dim: int):
        super().__init__()
        self.gate = nn.Parameter(torch.zeros(dim, dtype=torch.float32))

    def forward(self, x: Tensor) -> Tensor:
        g = torch.sigmoid(self.gate.to(dtype=x.dtype))[None, None, :]
        x_prev = torch.cat([torch.zeros_like(x[:, :1]), x[:, :-1]], dim=1)
        return (1 - g) * x + g * x_prev


class BigramHashEmbedding(nn.Module):
    """Hash consecutive token pairs into a learned embedding table."""
    def __init__(self, bigram_vocab_size: int, bigram_dim: int, model_dim: int):
        super().__init__()
        self.bigram_vocab_size = bigram_vocab_size
        self.embed = nn.Embedding(bigram_vocab_size, bigram_dim)
        nn.init.zeros_(self.embed.weight)
        self.proj = CastedLinear(bigram_dim, model_dim, bias=False) if bigram_dim != model_dim else None
        if self.proj is not None:
            nn.init.zeros_(self.proj.weight)
        self.scale = nn.Parameter(torch.tensor(0.05, dtype=torch.float32))

    def bigram_hash(self, tokens: Tensor) -> Tensor:
        t = tokens.to(torch.int32)
        mod = self.bigram_vocab_size - 1
        out = torch.empty_like(t)
        out[..., 0] = mod
        out[..., 1:] = torch.bitwise_xor(36313 * t[..., 1:], 27191 * t[..., :-1]) % mod
        return out.long()

    def forward(self, token_ids: Tensor) -> Tensor:
        h = self.embed(self.bigram_hash(token_ids))
        if self.proj is not None:
            h = self.proj(h)
        return h * self.scale.to(dtype=h.dtype)


class TrigramHashEmbedding(nn.Module):
    """Hash consecutive token triplets into a learned embedding table."""
    def __init__(self, trigram_vocab_size: int, trigram_dim: int, model_dim: int):
        super().__init__()
        self.trigram_vocab_size = trigram_vocab_size
        self.embed = nn.Embedding(trigram_vocab_size, trigram_dim)
        nn.init.zeros_(self.embed.weight)
        self.proj = CastedLinear(trigram_dim, model_dim, bias=False) if trigram_dim != model_dim else None
        if self.proj is not None:
            nn.init.zeros_(self.proj.weight)
        self.scale = nn.Parameter(torch.tensor(0.03, dtype=torch.float32))

    def trigram_hash(self, tokens: Tensor) -> Tensor:
        t = tokens.to(torch.int32)
        mod = self.trigram_vocab_size - 1
        out = torch.empty_like(t)
        out[..., 0] = mod
        out[..., 1] = mod
        out[..., 2:] = (
            48271 * t[..., 2:] ^ 36313 * t[..., 1:-1] ^ 27191 * t[..., :-2]
        ) % mod
        return out.long()

    def forward(self, token_ids: Tensor) -> Tensor:
        h = self.embed(self.trigram_hash(token_ids))
        if self.proj is not None:
            h = self.proj(h)
        return h * self.scale.to(dtype=h.dtype)


class Block(nn.Module):
    def __init__(self, dim: int, num_heads: int, num_kv_heads: int, mlp_mult: float,
                 rope_base: float, qk_gain_init: float, layer_idx: int = 0):
        super().__init__()
        self.attn_norm = RMSNorm()
        self.mlp_norm = RMSNorm()
        self.attn = CausalSelfAttention(dim, num_heads, num_kv_heads, rope_base, qk_gain_init, layer_idx=layer_idx)
        self.mlp = MLP(dim, mlp_mult)
        self.attn_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.mlp_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.resid_mix = nn.Parameter(torch.stack((torch.ones(dim), torch.zeros(dim))).float())

    def forward(self, x: Tensor, x0: Tensor, v0: Tensor | None = None) -> tuple[Tensor, Tensor]:
        """Returns (hidden_state, v) where v is the raw V from this layer's attention."""
        mix = self.resid_mix.to(dtype=x.dtype)
        x = mix[0][None, None, :] * x + mix[1][None, None, :] * x0
        attn_out, v = self.attn(self.attn_norm(x), v0=v0)
        x = x + self.attn_scale.to(dtype=x.dtype)[None, None, :] * attn_out
        x = x + self.mlp_scale.to(dtype=x.dtype)[None, None, :] * self.mlp(self.mlp_norm(x))
        return x, v


class GPT(nn.Module):
    def __init__(
        self,
        vocab_size: int,
        num_layers: int,
        model_dim: int,
        num_heads: int,
        num_kv_heads: int,
        mlp_mult: float,
        tie_embeddings: bool,
        tied_embed_init_std: float,
        logit_softcap: float,
        rope_base: float,
        qk_gain_init: float,
        bigram_vocab_size: int = 0,
        bigram_dim: int = 128,
        trigram_vocab_size: int = 0,
        trigram_dim: int = 64,
    ):
        super().__init__()
        if logit_softcap <= 0.0:
            raise ValueError(f"logit_softcap must be positive, got {logit_softcap}")
        self.tie_embeddings = tie_embeddings
        self.tied_embed_init_std = tied_embed_init_std
        self.logit_softcap = logit_softcap
        self.tok_emb = nn.Embedding(vocab_size, model_dim)
        self.bigram = BigramHashEmbedding(bigram_vocab_size, bigram_dim, model_dim) if bigram_vocab_size > 0 else None
        self.trigram = TrigramHashEmbedding(trigram_vocab_size, trigram_dim, model_dim) if trigram_vocab_size > 0 else None
        self.num_encoder_layers = num_layers // 2
        self.num_decoder_layers = num_layers - self.num_encoder_layers
        self.num_skip_weights = min(self.num_encoder_layers, self.num_decoder_layers)
        self.skip_weights = nn.Parameter(torch.ones(self.num_skip_weights, model_dim, dtype=torch.float32))
        self.smear = SmearGate(model_dim)
        self.blocks = nn.ModuleList(
            [
                Block(model_dim, num_heads, num_kv_heads, mlp_mult, rope_base, qk_gain_init, layer_idx=i)
                for i in range(num_layers)
            ]
        )
        self.final_norm = RMSNorm()
        self.lm_head = None if tie_embeddings else CastedLinear(model_dim, vocab_size, bias=False)
        if self.lm_head is not None:
            self.lm_head._zero_init = True
        self._init_weights()

    def _init_weights(self) -> None:
        if self.tie_embeddings:
            nn.init.normal_(self.tok_emb.weight, mean=0.0, std=self.tied_embed_init_std)
        num_layers = len(self.blocks)
        for name, module in self.named_modules():
            if isinstance(module, nn.Linear):
                if getattr(module, "_zero_init", False):
                    nn.init.zeros_(module.weight)
                elif module.weight.ndim == 2 and module.weight.shape[0] >= 64 and module.weight.shape[1] >= 64:
                    nn.init.orthogonal_(module.weight, gain=1.0)
                    if ".proj." in name or name.endswith(".proj"):
                        with torch.no_grad():
                            module.weight.mul_(1.0 / math.sqrt(2 * num_layers))

    def forward(self, input_ids: Tensor, target_ids: Tensor) -> Tensor:
        x = self.tok_emb(input_ids)
        if self.bigram is not None:
            x = x + self.bigram(input_ids)
        if self.trigram is not None:
            x = x + self.trigram(input_ids)
        x = F.rms_norm(x, (x.size(-1),))
        x = self.smear(x)
        x0 = x
        v0: Tensor | None = None  # Value residual cache from layer 0
        skips: list[Tensor] = []
        for i in range(self.num_encoder_layers):
            x, v = self.blocks[i](x, x0, v0=v0)
            if i == 0:
                v0 = v  # Cache V from first layer
            skips.append(x)
        for i in range(self.num_decoder_layers):
            if skips:
                x = x + self.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            x, v = self.blocks[self.num_encoder_layers + i](x, x0, v0=v0)
            if self.num_encoder_layers + i == 0:
                v0 = v
        x = self.final_norm(x).reshape(-1, x.size(-1))
        targets = target_ids.reshape(-1)
        if self.tie_embeddings:
            logits_proj = F.linear(x, self.tok_emb.weight)
        else:
            if self.lm_head is None:
                raise RuntimeError("lm_head is required when tie_embeddings=False")
            logits_proj = self.lm_head(x)
        logits = self.logit_softcap * torch.tanh(logits_proj / self.logit_softcap)
        return F.cross_entropy(logits.float(), targets, reduction="mean")

    def forward_logits(self, input_ids: Tensor) -> Tensor:
        x = self.tok_emb(input_ids)
        if self.bigram is not None:
            x = x + self.bigram(input_ids)
        if self.trigram is not None:
            x = x + self.trigram(input_ids)
        x = F.rms_norm(x, (x.size(-1),))
        x = self.smear(x)
        x0 = x
        v0: Tensor | None = None
        skips: list[Tensor] = []
        for i in range(self.num_encoder_layers):
            x, v = self.blocks[i](x, x0, v0=v0)
            if i == 0:
                v0 = v
            skips.append(x)
        for i in range(self.num_decoder_layers):
            if skips:
                x = x + self.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            x, v = self.blocks[self.num_encoder_layers + i](x, x0, v0=v0)
            if self.num_encoder_layers + i == 0:
                v0 = v
        x = self.final_norm(x)
        if self.tie_embeddings:
            logits_proj = F.linear(x, self.tok_emb.weight)
        else:
            logits_proj = self.lm_head(x)
        return self.logit_softcap * torch.tanh(logits_proj / self.logit_softcap)


def eval_val_sliding(
    args: Hyperparameters,
    base_model: nn.Module,
    rank: int,
    world_size: int,
    device: torch.device,
    val_tokens: Tensor,
    base_bytes_lut: Tensor,
    has_leading_space_lut: Tensor,
    is_boundary_token_lut: Tensor,
    stride: int,
    batch_seqs: int = 32,
) -> tuple[float, float]:
    seq_len = args.train_seq_len
    total_tokens = val_tokens.numel() - 1
    window_starts = [ws for ws in range(0, total_tokens, stride)
                     if min(ws + seq_len, total_tokens) - ws >= stride or ws == 0]
    total_windows = len(window_starts)
    my_s = (total_windows * rank) // world_size
    my_e = (total_windows * (rank + 1)) // world_size
    my_windows = window_starts[my_s:my_e]

    loss_sum = torch.zeros((), device=device, dtype=torch.float64)
    token_count = torch.zeros((), device=device, dtype=torch.float64)
    byte_count = torch.zeros((), device=device, dtype=torch.float64)

    base_model.eval()
    with torch.inference_mode():
        for bi in range(0, len(my_windows), batch_seqs):
            batch_ws = my_windows[bi:bi + batch_seqs]
            bsz = len(batch_ws)
            x_batch = torch.zeros(bsz, seq_len, dtype=torch.int64, device=device)
            y_batch = torch.zeros(bsz, seq_len, dtype=torch.int64, device=device)
            wlens: list[int] = []
            for i, ws in enumerate(batch_ws):
                end = min(ws + seq_len, total_tokens)
                wlen = end - ws
                wlens.append(wlen)
                chunk = val_tokens[ws:end + 1].to(dtype=torch.int64, device=device)
                x_batch[i, :wlen] = chunk[:-1]
                y_batch[i, :wlen] = chunk[1:]
            with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
                logits = base_model.forward_logits(x_batch)
            nll = F.cross_entropy(
                logits.reshape(-1, logits.size(-1)).float(),
                y_batch.reshape(-1),
                reduction="none",
            ).reshape(bsz, seq_len)
            for i, ws in enumerate(batch_ws):
                wlen = wlens[i]
                s = 0 if ws == 0 else max(wlen - stride, 0)
                scored_nll = nll[i, s:wlen].to(torch.float64)
                loss_sum += scored_nll.sum()
                token_count += float(wlen - s)
                tgt = y_batch[i, s:wlen]
                prev = x_batch[i, s:wlen]
                tb = base_bytes_lut[tgt].to(torch.float64)
                tb += (has_leading_space_lut[tgt] & ~is_boundary_token_lut[prev]).to(torch.float64)
                byte_count += tb.sum()
            if rank == 0 and (bi // batch_seqs) % 50 == 0:
                done = min(bi + batch_seqs, len(my_windows))
                pct = done / len(my_windows) * 100
                running_bpb = 0.0
                if token_count.item() > 0:
                    rl = (loss_sum / token_count).item()
                    running_bpb = rl / math.log(2.0) * (token_count.item() / byte_count.item())
                print(f"  sliding_eval [{pct:5.1f}%] {done}/{len(my_windows)} windows running_bpb={running_bpb:.6f}", flush=True)

    if dist.is_available() and dist.is_initialized():
        dist.all_reduce(loss_sum, op=dist.ReduceOp.SUM)
        dist.all_reduce(token_count, op=dist.ReduceOp.SUM)
        dist.all_reduce(byte_count, op=dist.ReduceOp.SUM)

    val_loss = (loss_sum / token_count).item()
    bits_per_token = val_loss / math.log(2.0)
    tokens_per_byte = token_count.item() / byte_count.item()
    base_model.train()
    return val_loss, bits_per_token * tokens_per_byte



def test_time_train(
    args: Hyperparameters,
    base_model: nn.Module,
    rank: int,
    world_size: int,
    device: torch.device,
    val_tokens: Tensor,
    base_bytes_lut: Tensor,
    has_leading_space_lut: Tensor,
    is_boundary_token_lut: Tensor,
    log_fn,
) -> tuple[float, float]:
    """Fine-tune model on validation tokens, then evaluate with sliding window."""
    log_fn(f"ttt:starting lr={args.ttt_lr} epochs={args.ttt_epochs}")

    ttt_model = copy.deepcopy(base_model)
    ttt_model.train()

    ttt_params = [p for p in ttt_model.parameters() if p.requires_grad]
    ttt_optimizer = torch.optim.AdamW(ttt_params, lr=args.ttt_lr, weight_decay=0.0)

    seq_len = args.train_seq_len
    total_seqs = (val_tokens.numel() - 1) // seq_len
    my_start = (total_seqs * rank) // world_size
    my_end = (total_seqs * (rank + 1)) // world_size

    for epoch in range(args.ttt_epochs):
        epoch_loss = 0.0
        epoch_tokens = 0
        for seq_idx in range(my_start, my_end):
            raw_start = seq_idx * seq_len
            raw_end = raw_start + seq_len + 1
            chunk = val_tokens[raw_start:raw_end].to(device=device, dtype=torch.int64)
            x = chunk[:-1].unsqueeze(0)
            y = chunk[1:].unsqueeze(0)
            ttt_optimizer.zero_grad(set_to_none=True)
            with torch.autocast(device_type="cuda", dtype=torch.bfloat16, enabled=True):
                loss = ttt_model(x, y)
            loss.backward()
            ttt_optimizer.step()
            epoch_loss += loss.item() * seq_len
            epoch_tokens += seq_len
        if dist.is_available() and dist.is_initialized():
            loss_t = torch.tensor(epoch_loss, device=device)
            tok_t = torch.tensor(float(epoch_tokens), device=device)
            dist.all_reduce(loss_t, op=dist.ReduceOp.SUM)
            dist.all_reduce(tok_t, op=dist.ReduceOp.SUM)
            epoch_loss = loss_t.item()
            epoch_tokens = int(tok_t.item())
        avg_loss = epoch_loss / max(epoch_tokens, 1)
        log_fn(f"ttt:epoch={epoch+1}/{args.ttt_epochs} loss={avg_loss:.4f}")

    ttt_model.eval()
    val_loss, val_bpb = eval_val_sliding(
        args, ttt_model, rank, world_size, device,
        val_tokens, base_bytes_lut, has_leading_space_lut, is_boundary_token_lut,
        stride=args.eval_stride, batch_seqs=args.eval_batch_seqs,
    )
    log_fn(f"ttt:final val_loss={val_loss:.4f} val_bpb={val_bpb:.4f}")
    return val_loss, val_bpb





class LoRAAdapter(nn.Module):
    def __init__(self, weight, lora_rank):
        super().__init__()
        d_out, d_in = weight.shape
        self.A = nn.Parameter(torch.randn(d_out, lora_rank, device=weight.device, dtype=weight.dtype) * 0.01)
        self.B = nn.Parameter(torch.zeros(lora_rank, d_in, device=weight.device, dtype=weight.dtype))
        self.weight_ref = weight

    def get_delta(self):
        return self.A @ self.B


def lora_ttt_eval(args, base_model, rank, world_size, device,
                  val_tokens, base_bytes_lut, has_leading_space_lut, is_boundary_token_lut,
                  log_fn, lora_rank=8, ttt_lr=1e-4, ttt_epochs=3, chunk_size=2048):
    seq_len = args.train_seq_len
    total_tokens = val_tokens.numel() - 1
    reset_interval = 65536

    target_modules = []
    for block in base_model.blocks:
        target_modules.append(("c_q", block.attn.c_q))
        target_modules.append(("c_v", block.attn.c_v))

    orig_weights = {id(m): m.weight.data.clone() for _, m in target_modules}
    adapters = [LoRAAdapter(m.weight, lora_rank) for _, m in target_modules]
    lora_params = []
    for adapter in adapters:
        lora_params.extend([adapter.A, adapter.B])
    optimizer = torch.optim.AdamW(lora_params, lr=ttt_lr, weight_decay=0.0)

    def apply_lora():
        for adapter, (_, m) in zip(adapters, target_modules):
            m.weight.data = orig_weights[id(m)] + adapter.get_delta().to(m.weight.dtype)

    def reset_lora():
        for adapter in adapters:
            adapter.A.data.normal_(std=0.01)
            adapter.B.data.zero_()
        for _, m in target_modules:
            m.weight.data = orig_weights[id(m)].clone()
        for state in optimizer.state.values():
            state.clear()

    loss_sum = torch.zeros((), device=device, dtype=torch.float64)
    tok_cnt = torch.zeros((), device=device, dtype=torch.float64)
    byte_cnt = torch.zeros((), device=device, dtype=torch.float64)
    tokens_since_reset = 0

    window_starts = list(range(0, total_tokens - seq_len + 1, chunk_size))
    total_windows = len(window_starts)
    my_s = (total_windows * rank) // world_size
    my_e = (total_windows * (rank + 1)) // world_size
    my_windows = window_starts[my_s:my_e]

    log_fn(f"lora_ttt: windows={len(my_windows)} rank={lora_rank} lr={ttt_lr} epochs={ttt_epochs}")

    base_model.eval()
    for wi, ws in enumerate(my_windows):
        end = min(ws + seq_len, total_tokens)
        wlen = end - ws
        chunk = val_tokens[ws:end + 1].to(device=device, dtype=torch.int64)
        x = chunk[:-1].unsqueeze(0)
        y = chunk[1:].unsqueeze(0)

        apply_lora()
        with torch.inference_mode():
            with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
                logits = base_model.forward_logits(x)
            nll = F.cross_entropy(
                logits.reshape(-1, logits.size(-1)).float(),
                y.reshape(-1), reduction="none",
            )
            s = 0 if ws == 0 else max(wlen - chunk_size, 0)
            scored = nll[s:wlen].to(torch.float64)
            loss_sum += scored.sum()
            tok_cnt += float(wlen - s)
            tgt = y.reshape(-1)[s:wlen]
            prev = x.reshape(-1)[s:wlen]
            tb = base_bytes_lut[tgt].to(torch.float64)
            tb += (has_leading_space_lut[tgt] & ~is_boundary_token_lut[prev]).to(torch.float64)
            byte_cnt += tb.sum()

        base_model.train()
        for _ in range(ttt_epochs):
            optimizer.zero_grad(set_to_none=True)
            apply_lora()
            with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
                loss = base_model(x.squeeze(0), y.squeeze(0))
            loss.backward()
            optimizer.step()
        base_model.eval()

        tokens_since_reset += wlen
        if tokens_since_reset >= reset_interval:
            reset_lora()
            tokens_since_reset = 0

        if rank == 0 and wi % 200 == 0:
            pct = (wi + 1) / len(my_windows) * 100
            rbpb = 0.0
            if tok_cnt.item() > 0:
                rl = (loss_sum / tok_cnt).item()
                rbpb = rl / math.log(2.0) * (tok_cnt.item() / byte_cnt.item())
            log_fn(f"  lora_ttt [{pct:5.1f}%] running_bpb={rbpb:.6f}")

    for _, m in target_modules:
        m.weight.data = orig_weights[id(m)]

    if dist.is_available() and dist.is_initialized():
        for t in (loss_sum, tok_cnt, byte_cnt):
            dist.all_reduce(t, op=dist.ReduceOp.SUM)

    vl = (loss_sum / tok_cnt).item()
    bpt = vl / math.log(2.0)
    tpb = tok_cnt.item() / byte_cnt.item()
    return vl, bpt * tpb


def main() -> None:
    global zeropower_via_newtonschulz5

    code = Path(__file__).read_text(encoding="utf-8")
    args = Hyperparameters()
    zeropower_via_newtonschulz5 = torch.compile(zeropower_via_newtonschulz5)

    distributed = "RANK" in os.environ and "WORLD_SIZE" in os.environ
    rank = int(os.environ.get("RANK", "0"))
    world_size = int(os.environ.get("WORLD_SIZE", "1"))
