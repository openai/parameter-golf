class HadamardRotation(nn.Module):
    """Hadamard rotation for outlier removal before quantization"""
    def __init__(self, hidden_size: int):
        super().__init__()
        self.hidden_size = hidden_size
        self.register_buffer("H", self._create_hadamard_matrix(hidden_size))
    
    def _create_hadamard_matrix(self, n: int) -> Tensor:
        """Create normalized Hadamard matrix"""
        k = int(math.ceil(math.log2(n)))
        size = 2 ** k
        H = torch.tensor([[1.0]])
        for _ in range(k):
            H = torch.kron(H, torch.tensor([[1.0, 1.0], [1.0, -1.0]]))
        H = H / math.sqrt(size)
        if size > n:
            H = H[:n, :n]
        return H
    
    def forward(self, x: Tensor) -> Tensor:
        H = self.H.to(x.device).to(x.dtype)
        return torch.matmul(x, H.t())


